// ============================================================
//   ТЕСТ В ДУХЕ ELITE — каркасный (wireframe) корабль для УКНЦ
//   Вращающийся 3D-объект с перспективной проекцией, рамка-визор
//   и редкие звёзды по краям. Управление: любая клавиша — выход.
//
//   Старое железо: экран НЕ чистим каждый кадр (cls в цикле нет).
//   «Подтираем за собой» — прошлый кадр перерисовываем цветом 0.
//   init(0) — один раз, только для настройки видеорежима.
//
//   3D-математика целочисленная:
//   - поворот вокруг Y и X через sin256/cos256 (масштаб 256);
//   - перспектива: делим на (z + DIST), DIST держит z>0;
//   - экран 320x264, центр (160,132), цвета 0..3.
// ============================================================

// Вершины корабля (стилизованный «Cobra»): нос, два хвоста,
// верх, низ, киль.  Координаты ~ -50..70.
int vx[6] = {  0, -50,  50,   0,   0,   0 };
int vy[6] = {  0,   0,   0,  25, -15,  30 };
int vz[6] = { 70, -40, -40, -25, -35, -45 };

// Рёбра — пары индексов вершин (12 линий каркаса).
int e0[12] = { 0, 0, 1, 0, 3, 3, 0, 4, 4, 3, 5, 5 };
int e1[12] = { 1, 2, 2, 3, 1, 2, 4, 1, 2, 5, 1, 2 };

// Спроецированные экранные координаты вершин (рабочие).
int sx[6];
int sy[6];

// Проекция всех вершин для углов a (рыскание) и b (тангаж).
void project(int a, int b) {
    int i, x, y, z;
    int ca, sa, cb, sb;
    int rx, rz, ry, rz2, zp;

    ca = cos256(a);  sa = sin256(a);
    cb = cos256(b);  sb = sin256(b);

    i = 0;
    while (i < 6) {
        x = vx[i];  y = vy[i];  z = vz[i];

        rx  = (x * ca - z * sa) / 256;        // поворот вокруг Y
        rz  = (x * sa + z * ca) / 256;
        ry  = (y * cb - rz * sb) / 256;       // поворот вокруг X
        rz2 = (y * sb + rz * cb) / 256;

        zp = rz2 + 300;                       // перспектива (z всегда > 0)
        sx[i] = 160 + (rx * 256) / zp;
        sy[i] = 132 - (ry * 256) / zp;

        i = i + 1;
    }
}

// Рисует/стирает рёбра уже спроецированного корабля.
void edges(int color) {
    int i;
    i = 0;
    while (i < 12) {
        line(sx[e0[i]], sy[e0[i]], sx[e1[i]], sy[e1[i]], color);
        i = i + 1;
    }
}

// Звёзды по краям (рисуются один раз, корабль их почти не задевает).
void stars() {
    point( 30,  24, 1);  point(292,  40, 1);  point( 18, 210, 1);
    point(300, 232, 1);  point( 60, 246, 1);  point(270, 196, 1);
    point( 22, 120, 1);  point(298, 132, 1);  point(150,  18, 1);
}

int main() {
    int a, b, ap, bp;

    init(0);                       // однократная настройка экрана

    rect(8, 6, 304, 252, 1);       // рамка-визор (рисуется один раз)
    stars();

    a = 0;  b = 0;
    project(a, b);                 // первый кадр
    edges(3);

    while (getkey() == 0) {
        ap = a;  bp = b;
        a = a + 4;                 // рыскание
        b = b + 1;                 // лёгкий тангаж

        project(ap, bp);  edges(0);    // стереть прошлый кадр
        project(a,  b);   edges(3);    // нарисовать новый (белый)

        vsync();                   // 50 Гц
    }

    return 0;
}
