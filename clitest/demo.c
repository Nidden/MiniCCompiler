// ============================================================
//   ТЕСТ «ПОНИМАЕМ ДРУГ ДРУГА» — красивая графика для УКНЦ
//   Анимированная цветная розетка (стринг-арт) + пульс в центре.
//   Управление: любая клавиша — выход.
//
//   Опираемся на факты проекта:
//   - экран 320x264, центр (160,132), цвета графики 0..3;
//   - sin256/cos256: угол 0..255 = полный круг, результат -256..256;
//   - литералы в Mini-C десятичные (256 = 256, не восьмеричное);
//   - vsync() — синхронизация на 50 Гц, getkey() — 0 если клавиш нет;
//   - графику и текстовый вывод (177566) НЕ смешиваем.
// ============================================================

int main() {
    int cx, cy;            // центр экрана
    int t;                 // фаза анимации
    int i, a, b;           // счётчик лучей и два угла
    int r1, r2, pr;        // радиусы: внешний, внутренний, пульс
    int ax, ay, bx, by;    // концы луча
    int col, cr;           // цвет луча и радиус центрального круга

    init(0);               // графический режим
    cls(0);

    cx = 160;
    cy = 132;
    t = 0;

    while (getkey() == 0) {
        cls(0);                                  // очистка кадра

        pr = (sin256(t * 2) * 40) / 256;         // пульсация -40..40
        r1 = 112;                                // внешний радиус
        r2 = 64 + pr;                            // внутренний радиус «дышит»

        i = 0;
        while (i < 16) {
            a = (i * 256) / 16 + t;              // равномерно по кругу + вращение
            b = a * 3 + t;                       // утроенный угол даёт лепестки

            ax = cx + (cos256(a) * r1) / 256;
            ay = cy + (sin256(a) * r1) / 256;
            bx = cx + (cos256(b) * r2) / 256;
            by = cy + (sin256(b) * r2) / 256;

            col = 1 + (i % 3);                   // чередуем цвета 1..3
            line(ax, ay, bx, by, col);

            i = i + 1;
        }

        cr = 6 + (cos256(t * 4) * 6) / 256;      // пульсирующий круг 0..12
        circle(cx, cy, cr, 3);

        t = t + 3;                               // скорость вращения
        vsync();                                 // ждём кадр (50 Гц)
    }

    return 0;
}
