// ============================================================
//   ТЕСТ «ПОНИМАЕМ ДРУГ ДРУГА» — красивая графика для УКНЦ
//   Анимированная цветная розетка (стринг-арт) + пульс в центре.
//   Управление: любая клавиша — выход.
//
//   ВАЖНО: экран НЕ чистим каждый кадр (cls в цикле нет — это
//   медленно и мигает на реальной УКНЦ). Вместо этого «подтираем
//   за собой»: прошлый кадр перерисовываем цветом фона (0), затем
//   рисуем новый. init(0) вызывается один раз — только для
//   настройки видеорежима.
//
//   Факты проекта, на которые опираемся:
//   - экран 320x264, центр (160,132), цвета графики 0..3;
//   - sin256/cos256: угол 0..255 = круг, результат -256..256;
//   - литералы Mini-C десятичные; vsync() — 50 Гц; getkey() 0=нет.
// ============================================================

int cx, cy;                 // центр экрана (глобальные)

// Рисует один кадр розетки для фазы t.
// erase != 0  → рисуем цветом фона 0 (стираем прошлый кадр).
void frame(int t, int erase) {
    int i, a, b;
    int r1, r2, pr;
    int ax, ay, bx, by;
    int col, cr;

    pr = (sin256(t * 2) * 40) / 256;        // пульсация -40..40
    r1 = 112;                               // внешний радиус
    r2 = 64 + pr;                           // внутренний «дышит»

    i = 0;
    while (i < 16) {
        a = (i * 256) / 16 + t;             // равномерно по кругу + вращение
        b = a * 3 + t;                      // утроенный угол → лепестки

        ax = cx + (cos256(a) * r1) / 256;
        ay = cy + (sin256(a) * r1) / 256;
        bx = cx + (cos256(b) * r2) / 256;
        by = cy + (sin256(b) * r2) / 256;

        if (erase) col = 0; else col = 1 + (i % 3);
        line(ax, ay, bx, by, col);

        i = i + 1;
    }

    cr = 6 + (cos256(t * 4) * 6) / 256;     // пульсирующий круг 0..12
    if (erase) circle(cx, cy, cr, 0); else circle(cx, cy, cr, 3);
}

int main() {
    int t, tprev;

    init(0);                // однократно: настройка видеорежима
    cx = 160;
    cy = 132;
    t = 0;

    frame(t, 0);            // первый кадр

    while (getkey() == 0) {
        tprev = t;
        t = t + 3;          // скорость вращения

        frame(tprev, 1);    // стереть прошлый кадр (цветом фона)
        frame(t, 0);        // нарисовать новый
        vsync();            // ждём кадр (50 Гц)
    }

    return 0;
}
